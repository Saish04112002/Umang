              <li>There are also some top private B.Arch institutes in the country that offers direct admission in this prestigious 5-year programme. These institutions reserve a certain number of seats under the management quota scheme and B.Arch aspirants who want to apply for direct admission can get into one of these colleges through the management quota seats.</li>
