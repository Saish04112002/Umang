              <li>The lower age limit to apply for MBBS programme is 17 years. </li>

              <li>The upper age limit to apply for MBBS programme is 25 years </li>

              <li>Candidates must also have cracked the required entrance exam for the admissions. In India, NEET-UG is the only accepted entrance test and mode of admission for all MBBS courses across the MBBS colleges in the nation. Therefore, any student who wishes to pursue the course must ensure that they secure a good rank in NEET-UG of the respective year of admission. Check out the steps and processes in the NEET-UG admission processes followed across India.</li>
